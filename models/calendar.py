class Calendar:
    def __init__(self, year, month, day, tag):
        self.year = year
        self.month = month
        self.day = day
        self.tag = tag

    def get_year(self):

        return self.year

    def get_month(self):

        return self.month

    def get_day(self):

        return self.day

    def get_tag(self):

        return self.tag


